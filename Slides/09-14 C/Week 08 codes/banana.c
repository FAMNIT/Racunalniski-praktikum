#include<stdio.h>

int main(){
	//local variable
	int num;

	//interactive input - asking an input from the user
	printf("Hello human, please enter a number: \n");

	//scanf as an input instruction
	scanf("%d", &num);

	//show what was just input
	printf("\n\n Oh you gave me %d!", num);
	return 0;
}//endmain
